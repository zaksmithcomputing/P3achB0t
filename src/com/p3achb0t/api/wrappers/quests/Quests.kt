    constructor(index: Int, startValue: Int, completeValue: Int) : this(index, -1, -1, startValue, completeValue) {}

    private val mask: Int
        get() = MASKS.get(upperBitIndex - shift)

    fun getState(ctx: Context): Int {
        return if (upperBitIndex == -1) ctx.vars.getVarbit(index) else ctx.vars.getVarbit(index) shr shift and mask
    }

    fun isStarted(ctx: Context): Boolean {
        return getState(ctx) >= startValue
    }

    fun isComplete(ctx: Context): Boolean {
        return getState(ctx) >= completeValue
    }

    override fun toString(): String {
        val sb = StringBuilder("Quest{")
        sb.append("name=").append(name)
        sb.append(", index=").append(index)
        sb.append(", upperBitIndex=").append(upperBitIndex)
        sb.append(", lowerBitIndex=").append(shift)
        sb.append(", mask=").append(Integer.toBinaryString(mask))
        sb.append(", startValue=").append(startValue)
        sb.append(", completeValue=").append(completeValue)
        sb.append('}')
        return sb.toString()
    }

    companion object {
        private val MASKS: IntArray = IntArray(32)

        init {
            var mask = 1
            var i = 0
            while (i < 32) {
                MASKS[i] = mask
                i++
                mask = mask shl 1 or 1
            }
        }
    }
